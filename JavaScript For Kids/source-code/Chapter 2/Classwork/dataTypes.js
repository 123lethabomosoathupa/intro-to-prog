// Joining strings together
var greeting = "Hello ";
var myName = "Nick";
greeting + myName;

// Finding the length of the string 
"Helloworld".length

//Getting the character
var myName = "Nick";
myName[0];

// Using the number data type and operators
1234 + 57 * 3 - 31 / 4;

// Post incrementation 
var highFives = 10;
highFives++;

//Pre decrement
var highFives = 10;
--highFives;

//Booleans
//&& AND operator
var hadShower = true;
var hasBackpack = false;
hadShower && hasBackpack;

// OR operator ||
var hasApple = true;
var hasOrange = false;
hasApple || hasOrange;


//Greater or Less THAN
var height = 65;
var heightRestriction = 60;
height > heightRestriction;

//Changing to Upper Case
"Hello there, how are you doing?".toUpperCase();

//Changing to Lower Case
"hELlo THERE, hOW ARE yOu doINg?".toLowerCase();

//Equal to ===
var stringNumber = 5;
var actualNumber = 3 ;
stringNumber === actualNumber;

//Double ==
var stringNumber = 5;
var actualNumber = 3;
stringNumber == actualNumber;

//null
